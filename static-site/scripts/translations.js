// Current language
let currentLanguage = localStorage.getItem('bergkau-language') || 'de';

// Language initialization
function initializeLanguage() {
    // Update language button
    updateLanguageButton();
    
    // Translate page content
    translatePage();
    
    // Populate technical specifications table
    populateSpecsTable();
    
    // Update HTML lang attribute
    document.documentElement.lang = currentLanguage;
}

// Update language button display
function updateLanguageButton() {
    const currentLangElement = document.getElementById('currentLang');
    if (currentLangElement) {
        const langMap = {
            'ru': 'RU',
            'en': 'EN', 
            'de': 'DE',
            'es': 'ES'
        };
        currentLangElement.textContent = langMap[currentLanguage] || 'DE';
    }
    
    // Update active language button in modal
    const languageButtons = document.querySelectorAll('.language-btn');
    languageButtons.forEach(btn => {
        const lang = btn.getAttribute('data-lang');
        if (lang === currentLanguage) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

// Translate page content
function translatePage() {
    const elementsToTranslate = document.querySelectorAll('[data-translate]');
    
    elementsToTranslate.forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = translations[currentLanguage] && translations[currentLanguage][key];
        
        if (translation) {
            // Handle different element types
            if (element.tagName === 'INPUT' && element.type !== 'submit') {
                element.placeholder = translation;
            } else if (element.tagName === 'TEXTAREA') {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        }
    });
}

// Populate technical specifications table
function populateSpecsTable() {
    const tableBody = document.getElementById('specsTableBody');
    if (!tableBody) return;
    
    tableBody.innerHTML = '';
    
    technicalSpecsData.forEach(spec => {
        const row = document.createElement('tr');
        
        // Skip description rows for table structure, handle them differently
        if (typeof spec.id === 'string' && spec.id.includes('desc')) {
            row.innerHTML = `
                <td colspan="5" class="description-row" style="font-style: italic; color: var(--text-secondary); padding-left: var(--spacing-lg);">
                    ${spec[currentLanguage] || spec.ru}
                </td>
            `;
        } else {
            row.innerHTML = `
                <td><strong>${spec.id}</strong></td>
                <td>${spec.de || '-'}</td>
                <td>${spec.en || '-'}</td>
                <td>${spec.es || '-'}</td>
                <td>${spec.ru || '-'}</td>
            `;
        }
        
        tableBody.appendChild(row);
    });
}

// Change language function
function changeLanguage(language) {
    currentLanguage = language;
    localStorage.setItem('bergkau-language', language);
    
    // Update page
    initializeLanguage();
    
    // Close modal
    toggleLanguageModal();
    
    // Smooth scroll to top
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

// Toggle language modal
function toggleLanguageModal() {
    const modal = document.getElementById('languageModal');
    if (modal) {
        modal.classList.toggle('hidden');
    }
}

// Close modal when clicking outside
document.addEventListener('click', function(event) {
    const modal = document.getElementById('languageModal');
    const languageToggle = document.querySelector('.language-toggle');
    
    if (modal && !modal.classList.contains('hidden')) {
        if (!modal.contains(event.target) && !languageToggle.contains(event.target)) {
            modal.classList.add('hidden');
        }
    }
});

// Initialize language on page load
document.addEventListener('DOMContentLoaded', initializeLanguage);